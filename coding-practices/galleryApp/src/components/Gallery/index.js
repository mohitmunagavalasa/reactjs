class Gallery extends Component {
  state = {
    activeTumbnailId: imagesList[0].id,
  }

  getFilteredImage = () => {
    const {activeTumbnailId} = this.state
    const filteredImage = imagesList.filter(
      imageDetails => imageDetails.id === activeTumbnailId,
    )
    return filteredImage
  }

  onUpdateState = tabId => {
    this.setState({
      activeTumbnailId: tabId,
    })
  }

  render() {
    const filteredImage = this.getFilteredImage()
    const {activeTumbnailId} = this.state

    return (
      <div className="bg-container">
        <div className="gallary-app-container">
          <img
            src={filteredImage[0].imageUrl}
            alt={filteredImage[0].imageAltText}
            className="active-image-style"
          />
          <h1 className="title">Nature Photography</h1>
          <p className="description">Nature Photography by Mohit</p>
          <ul className="tumbnails-container">
            {imagesList.map(imageDetails => (
              <ThumbnailItem
                key={imageDetails.id}
                imageDetails={imageDetails}
                onUpdateState={this.onUpdateState}
                isActive={activeTumbnailId === imageDetails.id}
              />
            ))}
          </ul>
        </div>
      </div>
    )
  }
}

export default Gallery
